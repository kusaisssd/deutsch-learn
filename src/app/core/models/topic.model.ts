/**
 * موضوع تعليمي (Topic) — يجمع جمل متعلقة بمجال معين.
 *
 * المواضيع تكمل المستويات (A1-B2) و لا تستبدلها:
 *   - المستوى يصف "صعوبة" الجملة
 *   - الموضوع يصف "مجال" الجملة (تقنية، تسوّق، ضمائر…)
 *
 * جملة واحدة قد تنتمي لأكثر من موضوع، أو لا تنتمي لأي موضوع.
 */
export interface Topic {
  /** معرّف فريد للموضوع، يُستخدم في URL و في حقل Sentence.topics */
  id: string;

  /** اسم الموضوع المعروض للمستخدم (إنجليزي) */
  title: string;

  /** وصف قصير يشرح ما الذي يغطّيه هذا الموضوع */
  description: string;

  /** أيقونة emoji للعرض في البطاقات */
  emoji: string;

  /**
   * شرح موسّع للقواعد النحوية المركزية في هذا الموضوع.
   * يُعرض في أعلى صفحة جمل الموضوع، اختياري.
   *
   * مثلاً للضمائر: شرح er/sie/es و حالاتها (Nominativ/Akkusativ/Dativ).
   */
  grammarOverview?: string;

  /**
   * لون Tailwind base للموضوع (مثلاً 'emerald', 'sky', 'amber').
   * يُستخدم في الـ template كـ `bg-{color}-100`, `text-{color}-700`، إلخ.
   *
   * ⚠️ Tailwind JIT يحتاج class كاملة في الـ source — لذلك سنستخدم
   * هذه القيمة فقط في classes مُكوّنة عبر [class] binding أو سنحدد
   * كل الـ classes يدوياً في الـ template.
   */
  color: string;
}
